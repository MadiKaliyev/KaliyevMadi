# Задание №1. Спроектировать программу для определения победителя на
# выборах.
# Пользователю предоставляется список кандидатов, каждый из голосующих делает свой
# выбор. Выбранный кандидат добавляется в список. В итоге имеется неизменяемый список
# кандидатов.
# Определить победителя, в зависимости от количества встречаемости в списке кандидата.
# Определить количество голосов победителя.
# В случае, если будет двое победителей, сделать сортировку по длине слова между ними и
# выбрать победителя с минимальным количеством букв в имени.
# Кандидаты в выборы: Аскаров, Бекмуханов, Ернур, Пешая, Карим, Шаримазданов и т.д.
# Вы отдаете голос за:
# Победитель выборов: Ернур.

candidates = ["Аскаров", "Бекмуханов", "Ернур", "Пешая", "Карим", "Шаримазданов"]
votes = {candidate: 0 for candidate in candidates}

while True:
    print("Кандидаты в выборы:", ", ".join(candidates))
    choice = input("Вы отдаете голос за (для завершения введите 'exit'): ").strip()

    if choice.lower() == 'exit':
        break

    if choice in candidates:
        votes[choice] += 1
        print("Ваш голос за", choice, "учтен.")
    else:
        print("Кандидат не найден, попробуйте еще раз.")

max_votes = max(votes.values())
winners = [candidate for candidate, vote_count in votes.items() if vote_count == max_votes]

if len(winners) == 1:
    winner = winners[0]
    print(f"Победитель выборов: {winner}. Количество голосов: {max_votes}")
else:
    winners.sort(key=len)
    winner = winners[0]
    print(f"Несколько победителей, выбран победитель с минимальной длиной имени: {winner}. Количество голосов: {max_votes}")
